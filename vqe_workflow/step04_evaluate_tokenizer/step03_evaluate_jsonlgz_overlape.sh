#!/bin/bash

# ==============================================================================
# ⚙️ 配置区域
# ==============================================================================

# 输入文件路径
INPUT_FILE="signal_none_mongoq30_tokenized_l1.jsonl.gz"

# N-Gram 大小 (通常设为 2 或 3)
NGRAM_SIZE=3

# Python 脚本名称
PYTHON_SCRIPT="step03_evaluate_jsonlgz_overlape.py"

# ==============================================================================
# 🚀 执行逻辑
# ==============================================================================

echo "=================================================="
echo "🚀 开始执行 Token 重叠率分析"
echo "=================================================="
echo "输入文件: $INPUT_FILE"
echo "N-Gram:   $NGRAM_SIZE"
echo "=================================================="

# 检查 Python 脚本是否存在
if [ ! -f "$PYTHON_SCRIPT" ]; then
    echo "❌ 错误: 找不到 Python 脚本 '$PYTHON_SCRIPT'"
    exit 1
fi

# 执行 Python 脚本
python3 "$PYTHON_SCRIPT" \
    -i "$INPUT_FILE" \
    -n "$NGRAM_SIZE"

echo ""
echo "✅ 任务执行完毕！"
