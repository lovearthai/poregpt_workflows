#!/bin/bash

# ==============================================================================
# ⚙️ 配置区域
# ==============================================================================

# 输入文件：包含 tokens_layered 和 tokens_based 字段的 jsonl.gz 文件
INPUT_FILE="/mnt/zzbnew/dnadata/movetable/signal_LB06.mongoq30.vqe340s147000.aligned.jsonl.gz"

# Python 脚本名称
PYTHON_SCRIPT="step0x5_analyze_fsq_high_layer_movetable_jsonlgz.py"

# 输出图片路径
OUTPUT_IMAGE="step0x5_high_layer_top${TOP_K}_analysis.png"

# 分析参数
TOP_K=10          # 要标注的高频点数量
MAX_READS=10000   # 读取的最大 Read 数量

# ==============================================================================
# 🚀 脚本功能说明
# ==============================================================================
# 该脚本用于分析 FSQ 编码中“高层坐标”（High Layer）在不同碱基上的分布。
# 结果输出柱状图并在终端打印每种碱基的 Top K 高频坐标。
# ==============================================================================

# ==============================================================================
# 🚀 执行逻辑
# ==============================================================================

echo "=================================================="
echo "📊 FSQ 高层坐标分布分析"
echo "=================================================="
echo "输入: $INPUT_FILE"
echo "输出: $OUTPUT_IMAGE"
echo "分析: 最多处理 $MAX_READS 条 Read，Top $TOP_K 高频点"
echo "=================================================="

# 检查脚本是否存在
if [ ! -f "$PYTHON_SCRIPT" ]; then
    echo "❌ 错误: 找不到 Python 脚本 '$PYTHON_SCRIPT'"
    exit 1
fi

# 执行分析
python3 "$PYTHON_SCRIPT" \
    -i "$INPUT_FILE" \
    -o "$OUTPUT_IMAGE" \
    -n "$MAX_READS" \
    -k "$TOP_K"

if [ $? -eq 0 ]; then
    echo -e "\n✅ 分析任务成功结束！"
    echo "📊 已生成图像: $OUTPUT_IMAGE"
else
    echo -e "\n❌ 分析失败，请检查报错日志。"
    exit 1
fi