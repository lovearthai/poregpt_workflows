#!/bin/bash

# ==============================================================================
# ⚙️ 配置区域
# ==============================================================================

# 输入文件：包含 tokens_layered 和 tokens_based 字段的 jsonl.gz 文件
INPUT_FILE="/mnt/zzbnew/dnadata/movetable/signal_LB06.mongoq30.vqe340s147000.aligned.jsonl.gz"

# Python 脚本名称
PYTHON_SCRIPT="step0x6_fsq_dim_heatmap.py"

# 输出图片路径
OUTPUT_IMAGE="step0x6_fsq_dimension_feature_map.png"

# 分析参数
MAX_READS=10000   # 读取的最大 Read 数量 (对于统计热图，10000条记录可得到稳定分布)

# ==============================================================================
# 🚀 脚本功能说明
# ==============================================================================
# 该脚本用于分析 FSQ 编码的多维量化结构：
# - 将高层坐标解码为 4 个维度，每个维度 5 个量化档位
# - 为每种碱基生成维度×档位热图
# - 比较不同碱基的维度偏好特征
# ==============================================================================

# ==============================================================================
# 🚀 执行逻辑
# ==============================================================================

echo "=================================================="
echo "🌡️ FSQ 维度解码与热图分析"
echo "=================================================="
echo "输入: $INPUT_FILE"
echo "输出: $OUTPUT_IMAGE"
echo "分析: 最多处理 $MAX_READS 条 Read"
echo "=================================================="

# 检查脚本是否存在
if [ ! -f "$PYTHON_SCRIPT" ]; then
    echo "❌ 错误: 找不到 Python 脚本 '$PYTHON_SCRIPT'"
    exit 1
fi

# 执行分析
python3 "$PYTHON_SCRIPT" \
    -i "$INPUT_FILE" \
    -o "$OUTPUT_IMAGE" \
    -n "$MAX_READS"

if [ $? -eq 0 ]; then
    echo -e "\n✅ 分析任务成功结束！"
    echo "📊 已生成热图: $OUTPUT_IMAGE"
    echo "💡 提示：观察热图中的高亮块，那是该碱基在 FSQ 空间中的特征'指纹'。"
else
    echo -e "\n❌ 分析失败，请检查报错日志。"
    exit 1
fi
