#!/bin/bash

# ==============================================================================
# 🚀 脚本功能说明
# ==============================================================================
# 该脚本用于分析 FSQ 编码的多维量化结构：
#
# - 将 High Layer 的 token_id 解码为多维量化表示
#   （例如 levels = [11,11,11,11] → 4维，每维11个level）
#
# - 为每种碱基（A/T/C/G/N）构建：
#     【维度 × 量化档位】统计矩阵
#
# - 对每个维度做列归一化：
#     👉 观察该维度内部不同 level 的分布
#
# - 输出 heatmap：
#     👉 展示不同碱基在 FSQ 空间中的“结构偏好”
#
# - 可用于分析：
#     ✔ 哪些维度 encode 碱基信息
#     ✔ 哪些 level 更重要
#     ✔ tokenizer 表达能力变化（625 vs 14641）
# ==============================================================================

# ==============================================================================
# ⚙️ 配置区域
# ==============================================================================

# 🔥 tokenizer 控制（唯一需要频繁修改的参数）
TOKENIZER_NAME="vqe342s036000l1"

# 输入文件（通过 tokenizer 自动拼接）
INPUT_FILE="/mnt/zzbnew/poregpt/dnadata/movetable/signal_LB06.shiftr4.mongoq30.${TOKENIZER_NAME}.aligned.jsonl.gz"

# Python 脚本路径
PYTHON_SCRIPT="scripts/step0x6_fsq_dim_heatmap_movetable_jsonlgz.py"

# 分析参数
MAX_READS=10000  # 读取的最大 Read 数量 (对于统计热图，10000条记录可得到稳定分布)

# FSQ 量化结构（决定码本大小）
# 示例：
#   5 5 5 5   → 625
#   11 11 11 11 → 14641
LEVELS="11 11 11 11"

# ==============================================================================
# 📁 输出配置（保持简单）
# ==============================================================================

# 所有输出统一放在一个目录
OUT_DIR="step0x6_fsq_dim_heatmap_movetable_jsonlgz"
mkdir -p "$OUT_DIR"

# 输出文件（带 tokenizer，避免覆盖）
OUTPUT_IMAGE="${OUT_DIR}/step0x6_fsq_dim_heatmap_${TOKENIZER_NAME}.png"

# 支持外部覆盖输出路径
if [ -n "$2" ]; then
    OUTPUT_IMAGE="$2"
fi

# ==============================================================================
# 🚀 执行逻辑
# ==============================================================================

echo "=================================================="
echo "🌡️ FSQ 维度解码与热图分析"
echo "=================================================="
echo "Tokenizer : $TOKENIZER_NAME"
echo "输入文件  : $INPUT_FILE"
echo "输出文件  : $OUTPUT_IMAGE"
echo "Levels    : $LEVELS"
echo "MAX_READS : $MAX_READS"
echo "=================================================="

# ------------------------------------------------------------------------------
# ✅ 输入检查
# ------------------------------------------------------------------------------

if [ ! -f "$INPUT_FILE" ]; then
    echo "❌ 错误: 找不到输入文件 '$INPUT_FILE'"
    exit 1
fi

# ------------------------------------------------------------------------------
# ✅ 脚本检查
# ------------------------------------------------------------------------------

if [ ! -f "$PYTHON_SCRIPT" ]; then
    echo "❌ 错误: 找不到 Python 脚本 '$PYTHON_SCRIPT'"
    exit 1
fi

# ------------------------------------------------------------------------------
# 🚀 执行 Python 分析
# ------------------------------------------------------------------------------

python3 "$PYTHON_SCRIPT" \
    -i "$INPUT_FILE" \
    -o "$OUTPUT_IMAGE" \
    -n "$MAX_READS" \
    --levels $LEVELS

# ------------------------------------------------------------------------------
# ✅ 结果检查
# ------------------------------------------------------------------------------

if [ $? -eq 0 ]; then
    echo -e "\n✅ 分析完成！"
    echo "📊 输出路径: $OUTPUT_IMAGE"
else
    echo -e "\n❌ 执行失败，请检查日志。"
    exit 1
fi
