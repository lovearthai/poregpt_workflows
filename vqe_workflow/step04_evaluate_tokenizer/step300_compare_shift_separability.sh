#!/bin/bash

# ==============================================================================
# 脚本名称:
#     run_step300_compare_shift.sh
#
# 功能:
#     对比 shift 前后 representation separability
#
# 输出:
#     metrics csv
#     summary txt
#     barplot png
#     heatmap png
# ==============================================================================


# ------------------------------------------------------------------------------
# 1. 输入文件配置
# ------------------------------------------------------------------------------

TOKENIZER_NAME="vqe342s036000l1"

BEFORE_CSV="step252_cal_bases_pattern_feature_noshift/step252_strategy_boundary_bnum1_block5_sigma3.csv"

AFTER_CSV="step252_cal_bases_pattern_feature/step252_strategy_boundary_bnum1_block5_sigma3.csv"


# ------------------------------------------------------------------------------
# 2. 输出目录
# ------------------------------------------------------------------------------

OUTPUT_DIR="step300_compare_shift_separability"

mkdir -p $OUTPUT_DIR


# ------------------------------------------------------------------------------
# 3. 距离度量方式
# ------------------------------------------------------------------------------

# 可选:
#   euclidean
#   cosine

DISTANCE_METRIC="euclidean"


# ------------------------------------------------------------------------------
# 4. 输出信息
# ------------------------------------------------------------------------------

echo "======================================================"
echo "🚀 开始进行 Shift Separability 分析"
echo "时间: $(date)"
echo "Before CSV: $BEFORE_CSV"
echo "After CSV:  $AFTER_CSV"
echo "Metric:     $DISTANCE_METRIC"
echo "Output Dir: $OUTPUT_DIR"
echo "======================================================"


# ------------------------------------------------------------------------------
# 5. 执行 Python 分析
# ------------------------------------------------------------------------------

python3 scripts/step300_compare_shift_separability.py \
    --before_csv "$BEFORE_CSV" \
    --after_csv "$AFTER_CSV" \
    --output_dir "$OUTPUT_DIR" \
    --metric "$DISTANCE_METRIC"


# ------------------------------------------------------------------------------
# 6. 状态检查
# ------------------------------------------------------------------------------

if [ $? -eq 0 ]; then

    echo "======================================================"
    echo "✅ Separability 分析成功"
    echo "结果目录:"
    echo "$OUTPUT_DIR"

else

    echo "======================================================"
    echo "❌ Separability 分析失败"
    exit 1

fi